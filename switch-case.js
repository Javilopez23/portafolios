let valor;
valor = parseInt(prompt("ingrese un numero entre 1 y 3"));

switch (valor) {
    case 1:
        document.write ("ingreso uno");
        break;

    case 2:
        document.write ("ingreso un dos")
        break;

    case 3:
        document.write ("ingreso un tres")
        break;
   
        default:
        document.write(" No es un numero valido")
        break;
            
        }
  